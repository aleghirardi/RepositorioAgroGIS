    private void btnAceptarEdicionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarEdicionActionPerformed
        txtProvincia.setEnabled(true);
        DefaultTableModel modeloT = (DefaultTableModel) tblProvincia.getModel();
        int fila = tblProvincia.getSelectedRow();
        txtProvincia.setText((String) modeloT.getValueAt(fila, 1));
        Iterator ite = gestorH.listarClase(Provincia.class).iterator();
        while (ite.hasNext()) {
            Provincia p = (Provincia) ite.next();
            if (p.getNombreProvincia().equalsIgnoreCase(txtProvincia.getText())) {
                idProvincia = p.getIdProvincia();
            }
        }
        modeloT.removeRow(fila);
    }//GEN-LAST:event_btnAceptarEdicionActionPerformed

